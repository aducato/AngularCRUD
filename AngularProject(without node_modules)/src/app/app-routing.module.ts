import { NgModule } from '@angular/core';
import { RouterModule, Routes } from '@angular/router';
import { PageNotFoundComponent } from './page-not-found.component';
import { CreateStudentCanDeactivateGuardService } from './Student/create-student-can-deactivate-guard.service';
import { CreateStudentComponent } from './Student/create-student.component';
import { StudentDetailsComponent } from './Student/student-details.component';
import { StudentDetailsGuardService } from './Student/student-details.guard.service';
import { StudentListResolverService } from './Student/student-list-resolver.service';
import { StudentsListComponent } from './Student/students-list.component';


const routes: Routes = [
        
      {
        path: 'list',
        component: StudentsListComponent,
        resolve : {
          studentList : StudentListResolverService
        }
      
      },
      {
        path: 'edit/:id',
        component: CreateStudentComponent,
        canDeactivate: [
          CreateStudentCanDeactivateGuardService
        ]
      },
      {
        path: 'student/:id',
        component: StudentDetailsComponent,
        pathMatch: 'full',
        canActivate:[
          StudentDetailsGuardService
        ]
 
      },
      {
        path: '',
        component: StudentsListComponent,
        pathMatch: 'full',
        resolve : {
          studentList : StudentListResolverService
         }
      },
      {
        path:'notfound',
        component: PageNotFoundComponent
      }
    
];

@NgModule({
  imports: [RouterModule.forRoot(routes)],
  exports: [RouterModule]
})
export class AppRoutingModule { }
