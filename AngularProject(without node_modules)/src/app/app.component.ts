import { Component } from '@angular/core';
import { NavigationStart, Router, Event, NavigationEnd, NavigationError, NavigationCancel, RoutesRecognized, ResolveStart, ChildActivationStart } from '@angular/router';

@Component({
  selector: 'app-root',
  templateUrl: './app.component.html',
  styleUrls: ['./app.component.css']
})
export class AppComponent {
  title = 'CRUDonStudentData';


  showLoadingIndicator = true;

  constructor(private _router : Router){
    this._router.events.subscribe(
      (routerEvent : Event) => {
        if(routerEvent instanceof NavigationStart || routerEvent instanceof ChildActivationStart){

          this.showLoadingIndicator = true;
        }
        if(routerEvent instanceof NavigationEnd || 
          routerEvent instanceof NavigationError  ||
          routerEvent instanceof NavigationCancel
          ){

          this.showLoadingIndicator = false;
        }
      }



    );
  }


}
